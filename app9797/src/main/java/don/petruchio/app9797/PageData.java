package don.petruchio.app9797;

public class PageData {

    private String name;
    private String number;

    public PageData()
    {

    }
    public PageData(String name, String number)
    {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }
}
